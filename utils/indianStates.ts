/**
 * Get state by GST code (first 2 digits of GSTIN)
 */
export function getStateByGSTCode(gstCode: string): IndianState | undefined {
  return INDIAN_STATES.find((state) => state.gstCode === gstCode);
}

/**
 * Get state by state code (ISO code)
 */
export function getStateByCode(code: string): IndianState | undefined {
  return INDIAN_STATES.find((state) => state.code === code);
}
